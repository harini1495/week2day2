package week2.day2;

public class LearnLoopingStatement {
	
public static void main(String[] args) {
	
	//While,doWhile and for loop Example
	
	//1.doWhile
	
	int i = 5;
	do {
		
		System.out.println(i);
		
	}while(i>10);
	
	System.out.println("--------------------");
	
	//2.While loop 
	
	int num = 90;
	
	while(num<100) 
	{
		System.out.println(num);
		num++;
	}
			
	System.out.println("--------------------");
	
	//3.For loop
	
	for(int i1 =1;i1<10;i1++) {
		
		System.out.println(i1);
	}
	
}
 
}
